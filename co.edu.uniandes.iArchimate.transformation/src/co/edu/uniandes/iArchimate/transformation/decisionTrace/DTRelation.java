package co.edu.uniandes.iArchimate.transformation.decisionTrace;

public class DTRelation {

	private String from;
	private String to;
	public DTRelation(String from, String to) {
		super();
		this.from = from;
		this.to = to;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	
	
}
